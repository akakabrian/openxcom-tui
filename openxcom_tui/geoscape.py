    ".................####################...............####################..###########..................................",
    "...................##################................##################..##########....................................",
    "....................################...................###############..########.......................................",
    ".....................###############.....................#############..######.........................................",
    ".....................##############.......................###########..####.............................................",
    "......................#############.........................#########..##..............................................",
    "......................############...........................########.................................................",
    ".......................##########.............................#######.................................................",
    "........................########................................######................................................",
    ".........................######..................................#####................................................",
